                            also volunteer time with the children.
                            Education for Watu children is of the highest importance.  During 2020, Haven of Hope was able to a provide critical
                            technology package so their children could attend primary or secondary school online during the pandemic--laptops, wide angle 
                            webcams, projectors, cables, screens and internet connection.   Being technology enabled allows not only education instruction, 
                            but also trauma-care training and tele-medicine resources now available to the orphanage.
                            Sustainability-minded, Watu raises chickens for food and to sell eggs to the community. In 2017, Watu installed shipping 
                            containers on the property and have been renting the space to merchants, providing Watu income to help pay education costs.
